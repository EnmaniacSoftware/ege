#include "SwfRemoveObjectTag.h"

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
SwfRemoveObjectTag::SwfRemoveObjectTag() : SwfTag(),
                                           m_characterId(0),
                                           m_depth(0)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
SwfRemoveObjectTag::~SwfRemoveObjectTag()
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! Returns character ID. */
quint16 SwfRemoveObjectTag::characterId() const
{
  return m_characterId;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! Returns depth. */
quint16 SwfRemoveObjectTag::depth() const
{
  return m_depth;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! SwfTag override. Reads data from file. */
bool SwfRemoveObjectTag::read(SwfDataStream& data)
{
  data >> m_characterId;
  data >> m_depth;

  return QDataStream::Ok == data.status();
}
