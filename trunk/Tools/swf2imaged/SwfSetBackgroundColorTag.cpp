#include "SwfSetBackgroundColorTag.h"

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
SwfSetBackgroundColorTag::SwfSetBackgroundColorTag() : SwfTag(),
                                                       m_color(Qt::white)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
SwfSetBackgroundColorTag::~SwfSetBackgroundColorTag()
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! SwfTag override. Reads data from file. */
bool SwfSetBackgroundColorTag::read(SwfDataStream& data)
{
  m_color = data.readRGB();

  return QDataStream::Ok == data.status();
}
