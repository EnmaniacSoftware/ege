#include "Projects/Project.h"
#include "ObjectPool.h"

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
Project::Project(QObject* parent, const QString& typeName, const QString& name, const QString& path) :  QObject(parent),
                                                                                                        m_typeName(typeName),
                                                                                                        m_name(name),
                                                                                                        m_path(path),
                                                                                                        m_dirty(false)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
Project::~Project()
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void Project::setDirty(bool set)
{
  if (m_dirty != set)
  {
    m_dirty = set;

    emit dirtyFlagChanged();
  }
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
bool Project::isDirty() const
{
  return m_dirty;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void Project::onProjectDataChanged()
{
  setDirty(true);
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const QString& Project::typeName() const
{
  return m_typeName;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const QString& Project::name() const
{
  return m_name;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const QString& Project::path() const
{
  return m_path;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
