#include "core.h"

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
Core::Core()
{
  m_mainWindow = new MainWindow();
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
Core::~Core()
{
  if (NULL != m_mainWindow)
  {
    delete m_mainWindow;
    m_mainWindow = NULL;
  }
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! Returns core instance. */       
Core* Core::instance()
{
  static Core core;
  return &core;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
/*! Returns main window. */
MainWindow* Core::mainWindow() const
{
  return m_mainWindow;
}
