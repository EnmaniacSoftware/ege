//--------------------------------------------------------------------------------------------------------------------------------------------------------------
VertexBuffer::VertexBuffer(Application* app, u32 uid) : Object(app, uid), 
                                                        m_locked(false), 
                                                        m_usage(NVertexBuffer::UT_STATIC_WRITE)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
VertexBuffer::~VertexBuffer()
{
  destroy();
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const VertexDeclaration& VertexBuffer::vertexDeclaration() const
{
  return m_vertexDeclaration;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void VertexBuffer::setVertexDeclaration(const VertexDeclaration& vertexDeclaration)
{
  m_vertexDeclaration = vertexDeclaration;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void VertexBuffer::destroy()
{
  clear();
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void VertexBuffer::clear()
{
  m_locked = false;

  m_vertexDeclaration.clear();
}
