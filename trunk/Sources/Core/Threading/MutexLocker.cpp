//--------------------------------------------------------------------------------------------------------------------------------------------------------------
MutexLocker::MutexLocker(PMutex mutex) : m_mutex(mutex)
{
  m_mutex->lock();
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
MutexLocker::~MutexLocker()
{
  m_mutex->unlock();
}
