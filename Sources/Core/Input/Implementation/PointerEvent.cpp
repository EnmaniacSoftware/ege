PointerEvent::PointerEvent(PointerAction action, s32 x, s32 y, u32 uid) 
: Object(uid)
, m_action(action)
, m_x(x)
, m_y(y)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
PointerEvent::~PointerEvent()
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
PointerAction PointerEvent::action() const 
{ 
  return m_action; 
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
s32 PointerEvent::x() const 
{ 
  return m_x; 
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
s32 PointerEvent::y() const 
{ 
  return m_y; 
}
