EGE::File& File::operator << (u32 value)
{
  u8 data[4] = { (u8)(value & 0x000000ff), (u8)((value & 0x0000ff00) >> 8), (u8)((value & 0x00ff0000) >> 16), (u8)((value & 0xff000000) >> 24) };

  DataBuffer buf(&data, sizeof (data));
  p_func()->write(buf, sizeof (value));

  return *this;
}
