//--------------------------------------------------------------------------------------------------------------------------------------------------------------
EGE::File& File::operator << (u16 value)
{
  u8 data[2] = { static_cast<u8>(value & 0x00ff), static_cast<u8>((value & 0xff00) >> 8) };

  DataBuffer buf(&data, sizeof (data));
  p_func()->write(buf, sizeof (value));

  return *this;
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
