u32 Hash::FromString(const String& string)
{
  return Hash::FromData(reinterpret_cast<const u8*>(string.toAscii()), static_cast<s32>(string.length()));
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
u32 Hash::FromData(const u8* data, s32 size)
{
  u32 h = 0;
  u32 g;

  while (size--) 
  {
    h = (h << 4) + *data++;
    g = h & 0xf0000000;
    h ^= g >> 23;
    h &= ~g;
  }

  return h;
}
