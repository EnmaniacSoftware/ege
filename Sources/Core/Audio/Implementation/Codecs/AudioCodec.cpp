//--------------------------------------------------------------------------------------------------------------------------------------------------------------
AudioCodec::AudioCodec(u32 uid, const PDataBuffer& stream) : Object(NULL, uid)
                                                           , m_stream(stream)
                                                           , m_streamOffset(0)
                                                           , m_channels(0)
                                                           , m_frequency(0)
                                                           , m_bitsPerSample(0)
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
AudioCodec::~AudioCodec()
{
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
s32 AudioCodec::channels() const 
{ 
  return m_channels; 
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
s32 AudioCodec::frequency() const 
{ 
  return m_frequency; 
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
s32 AudioCodec::bitsPerSample() const 
{ 
  return m_bitsPerSample; 
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const PObject& AudioCodec::stream() const 
{ 
  return m_stream; 
}
