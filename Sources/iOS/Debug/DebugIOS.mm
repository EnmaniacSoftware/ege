#include "EGEDebug.h"
#import <Foundation/Foundation.h>
#include <assert.h>

EGE_NAMESPACE_BEGIN

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void Debug::Assert(const char* assertion, const char* file, int line)
{
  EGE_UNUSED(file);
  EGE_UNUSED(line);
  
  assert(false && assertion);
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
void Debug::Print(const char* text)
{
  NSLog(@"%s", text);
}
